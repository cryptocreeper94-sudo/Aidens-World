// Chronoverse Service Worker — Offline Support
const CACHE_NAME = 'chronoverse-v1';
const OFFLINE_URLS = [
  '/',
  '/index.html',
  '/src/systems/SaveSystem.js',
  '/src/systems/SoundFX.js',
  '/src/systems/MusicSystem.js',
  '/src/systems/LevelData.js',
  '/src/scenes/BootScene.js',
  '/src/scenes/HubScene.js',
  '/src/scenes/LevelScene.js',
  '/src/config.js',
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      return cache.addAll(OFFLINE_URLS).catch(() => {});
    })
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) => {
      return Promise.all(
        keys.filter((k) => k !== CACHE_NAME).map((k) => caches.delete(k))
      );
    })
  );
  self.clients.claim();
});

self.addEventListener('fetch', (event) => {
  if (event.request.url.includes('/api/') || event.request.url.includes('firebasejs')) {
    return;
  }

  event.respondWith(
    fetch(event.request)
      .then((response) => {
        if (response.ok) {
          const clone = response.clone();
          caches.open(CACHE_NAME).then((cache) => {
            cache.put(event.request, clone);
          });
        }
        return response;
      })
      .catch(() => {
        return caches.match(event.request).then((cached) => {
          return cached || caches.match('/index.html');
        });
      })
  );
});
